    private void TablaDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaDatosMouseClicked
       int fila= TablaDatos.getSelectedRow();
       if (fila==-1){
           JOptionPane.showMessageDialog(null,"Usuario no seleccionado");
       }else{
           id= Integer.parseInt(TablaDatos.getValueAt(fila,0).toString());
           String nombre= (String)TablaDatos.getValueAt(fila,1);
           String apellido= (String)TablaDatos.getValueAt(fila,2);
           dni= Integer.parseInt(TablaDatos.getValueAt(fila,3).toString());
           txtId.setText(""+id);
           txtNombre.setText(nombre);
           txtApellido.setText(apellido);
           txtDni.setText(""+dni);
       }
    }//GEN-LAST:event_TablaDatosMouseClicked

