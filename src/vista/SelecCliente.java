        void listar(){
        String sql= "select * from persona";
        try {
            cn=con.getConexion();
            st=cn.createStatement();
            rs=st.executeQuery(sql);
            Object[]persona= new Object[4];
            modelo=(DefaultTableModel)TablaClientes.getModel();
            while (rs.next()){
                persona[0]=rs.getString("id");
                persona[1]=rs.getString("nombre");
                persona[2]=rs.getString("apellido");
                persona[3]=rs.getInt("dni");
                modelo.addRow(persona);
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }
    void listarRemitos(int id){
        String sql= "select * from remito where idCliente="+id;
        try {
            cn=con.getConexion();
            st=cn.createStatement();
            rs=st.executeQuery(sql);
            Object[]remito= new Object[5];
            modelo=(DefaultTableModel)TablaRemito.getModel();
            while (rs.next()){
                remito[0]=rs.getString("id");
                remito[1]=rs.getString("idcliente");
                remito[2]=rs.getDate("fecha");
                remito[3]=rs.getInt("importe");
                remito[4]=rs.getInt("pagado");
                modelo.addRow(remito);
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }
      void LimpiarTablaRemitos(){
         for (int i=0;i<TablaRemito.getRowCount();i++)
            {
               modelo.removeRow(i);
                i=i-1;
             }
      }
       void AgregarRemito(){
          String dia= txtDiaremito.getText();
          String mes= txtMesremito.getText();
          String anio= txtAnioremito.getText();
          String importe= txtMesremito.getText();
          String idCliente = txtIdcliente.getText();
          String date= anio+"-"+mes+"-"+ dia;
  
          if (dia.equals("") || mes.equals("") || anio.equals("") || importe.equals("")){
            JOptionPane.showMessageDialog(null, "Los datos estÃ¡n vacios");
            }
          else{
              String sql= "insert into remito(fecha,idcliente,importe) values ('"+date+"','"+idCliente+"','"+importe+"')";
              try {
                  cn = con.getConexion();
                  st=cn.createStatement();
                  st.executeUpdate(sql);
                  JOptionPane.showMessageDialog(null, "Usuario agregado");
                  LimpiarTablaRemitos();
                  listar();
              } catch (Exception e) {
                  System.out.println(e);
              }
              
          }
      }

